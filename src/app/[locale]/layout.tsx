import TailwindScreenIndicator from "@/components/atoms/TailwindIndicator";
import Header from "@/components/Header";
import Background from "@/components/organisms/Background";
import { routing } from "@/i18n/routing";
import { ThemeProvider } from "@/providers/ThemeProvider";
import type { Metadata } from "next";
import { hasLocale, NextIntlClientProvider } from "next-intl";
import { Lexend, Montserrat, Oswald } from "next/font/google";
import { notFound } from "next/navigation";
import "../globals.css";
import { cn } from "../utils";

const montserrat = Montserrat({
  variable: "--font-montserrat",
  subsets: ["latin"],
});

const oswald = Oswald({
  variable: "--font-oswald",
  subsets: ["latin"],
});

const lexend = Lexend({
  variable: "--font-lexend",
  subsets: ["latin"],
});

export const metadata: Metadata = {
  title: "Simon Camacho",
  description: "Generated by create next app",
};

type LocaleLayoutProps = {
  children: React.ReactNode;
  params: Promise<{ locale: string }>;
};

const LocaleLayout = async ({ children, params }: LocaleLayoutProps) => {
  const { locale } = await params;
  if (!hasLocale(routing.locales, locale)) {
    notFound();
  }

  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={cn(
          `${montserrat.variable} ${oswald.variable} ${lexend.variable} antialiased`,
          "[&_*:focus-visible]:outline-accent [&_*:focus-visible]:outline-offset-3 [&_*:focus-visible]:outline-dashed"
        )}
      >
        <NextIntlClientProvider>
          <ThemeProvider
            attribute="class"
            defaultTheme="system"
            enableSystem
            disableTransitionOnChange
          >
            <Background />
            <Header />
            {children}
            <TailwindScreenIndicator />
          </ThemeProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
};

export default LocaleLayout;
